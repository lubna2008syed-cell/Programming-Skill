# 1. Count up from 0 to 50 (increments of 1)
print("Counting up from 0 to 50:")
for i in range(0, 51, 1):
    print(i, end=" ")
print("\n")  # New line for separation

# 2. Count down from 50 to 0 (decrements of 1)
print("Counting down from 50 to 0:")
for i in range(50, -1, -1):
    print(i, end=" ")
print("\n")

# 3. Count up from 30 to 50 (increments of 1)
print("Counting up from 30 to 50:")
for i in range(30, 51, 1):
    print(i, end=" ")
print("\n")

# 4. Count down from 50 to 10 (decrements of 2)
print("Counting down from 50 to 10 in steps of 2:")
for i in range(50, 9, -2):
    print(i, end=" ")
print("\n")

# 5. Count up from 100 to 200 (increments of 5)
print("Counting up from 100 to 200 in steps of 5:")
for i in range(100, 201, 5):
    print(i, end=" ")
print("\n")